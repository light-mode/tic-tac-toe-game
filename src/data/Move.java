package data;

import java.io.Serializable;

/**
 *
 * @author ADMIN
 */
public class Move implements Serializable {

    private final int row;
    private final int column;

    public Move(int row, int column) {
        this.row = row;
        this.column = column;
    }

    public int getRow() {
        return row;
    }

    public int getColumn() {
        return column;
    }
}
